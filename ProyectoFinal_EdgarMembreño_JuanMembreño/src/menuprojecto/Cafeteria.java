    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (tabla.getSelectedRow() >= 0) { // valida que se elige una columna de la table
            Object numeroO = tabla.getValueAt(tabla.getSelectedRow(), 4);
            int num = (int) numeroO;
            int num2 = Integer.parseInt(total.getText());
            int tot = num2 - num;
            total.setText(Integer.toString(tot));

            lista.remove(tabla.getSelectedRow());
            DefaultTableModel x = (DefaultTableModel) tabla.getModel();
            x.removeRow(tabla.getSelectedRow());
            //tabla.setModel(x);
            JOptionPane.showMessageDialog(null, "Se elimino exitosamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error! No es posible");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

