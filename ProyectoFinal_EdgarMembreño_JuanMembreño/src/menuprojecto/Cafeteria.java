    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String nombre1, apellido1, comida1 = "", bebida1 = "";
            int precio = 0, cuenta1;
            nombre1 = nombre.getText();
            apellido1 = apellido.getText();
            cuenta1 = Integer.parseInt(cuenta.getText());
            if (hamburguesa.isSelected()) {
                comida1 = "Hamburguesa";
                precio = 20;
            } else if (pizza.isSelected()) {
                comida1 = "Pizza";
                precio = 15;
            } else if (pollo.isSelected()) {
                comida1 = "Pollo";
                precio = 25;
            } else if (tacos.isSelected()) {
                comida1 = "Tacos";
                precio = 25;
            } else if (pasta.isSelected()) {
                comida1 = "Pasta";
                precio = 30;
            } else if (hotdog.isSelected()) {
                comida1 = "Hot dog";
                precio = 20;
            } else if (pastel.isSelected()) {
                comida1 = "Pastel";
                precio = 30;
            } else if (helado.isSelected()) {
                comida1 = "Helado";
                precio = 20;
            } else if (cheesecake.isSelected()) {
                comida1 = "Cheesecake";
                precio = 15;
            } else if (chocolate.isSelected()) {
                comida1 = "Chocolate";
                precio = 20;
            } else if (galletas.isSelected()) {
                comida1 = "Galletas";
                precio = 15;
            } else if (churros.isSelected()) {
                comida1 = "Churros";
                precio = 15;
            }
            if (agua.isSelected()) {
                bebida1 = "Hot dog";
                precio += 12;
            } else if (soda.isSelected()) {
                bebida1 = "Soda";
                precio += 20;
            } else if (cafe.isSelected()) {
                bebida1 = "Cafe";
                precio += 10;
            } else if (te.isSelected()) {
                bebida1 = "Té";
                precio += 14;
            } else if (malteada.isSelected()) {
                bebida1 = "Malteada";
                precio += 12;
            } else if (bebidaenergetica.isSelected()) {
                bebida1 = "Bebida energetica ";
                precio += 25;
            }
            int num = Integer.parseInt(total.getText());
            int tot = precio + num;
            total.setText(Integer.toString(tot));
            nombre.setText("");
            apellido.setText("");
            cuenta.setText("");
            lista.add(new arreglos_arraylist(nombre1, cuenta1, comida1, bebida1, precio, apellido1));
            Object[] nuevo = {nombre1, cuenta1, comida1, bebida1, precio};
            DefaultTableModel t = (DefaultTableModel) tabla.getModel();
            t.addRow(nuevo);
            tabla.setModel(t);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

